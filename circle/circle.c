#include <stdio.h>

#define N 32
#define R 12

/*       _________
      _#############_
    _#################_
   #####################
  #######################
  #######################
  #######################
  #######################
  ^#####################^
   ^###################^
     ^###############^
       ^^#########^^     */

int main() {

        for (int j = N/2; j > N/2-N; j-=2) {
            for (int i = N/2; i > N/2-N; i--) {

                unsigned char a = i*i+j*j < R*R;
                unsigned char b = i*i+(j-1)*(j-1) < R*R;
                putc(0x477AFA0>>7*((a<<1)|b)&0x7f,stdout);
            }
            putc('\n',stdout);            
        }

    return 0;
}